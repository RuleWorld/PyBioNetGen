    def getReactionCenter(self, reactant, product, translator):
        rcomponent = Counter()
        pcomponent = Counter()

        for element in reactant:
            if element[0] in translator:
                for molecule in translator[element[0]].molecules:
                    for component in molecule.components:
                        molecule.sort()
                        rcomponent.update(
                            Counter(
                                [
                                    (
                                        molecule.name,
                                        component.name,
                                        len(component.bonds) > 0,
                                        component.activeState,
                                    )
                                ]
                            )
                        )
        for element in product:
            if element[0] in translator:
                for molecule in translator[element[0]].molecules:
                    molecule.sort()
                    for component in molecule.components:
                        molecule.sort()
                        pcomponent.update(
                            Counter(
                                [
                                    (
                                        molecule.name,
                                        component.name,
                                        len(component.bonds) > 0,
                                        component.activeState,
                                    )
                                ]
                            )
                        )
        reactionCenter = [
            (x[0], x[1])
            for x in rcomponent
            for y in pcomponent
            if (x[0], x[1]) == (y[0], y[1])
            and x != y
            and rcomponent[x] != pcomponent[y]
        ]
        rreactionCenter = [
            (x[0], x[1])
            for x in pcomponent
            for y in rcomponent
            if (x[0], x[1]) == (y[0], y[1])
            and x != y
            and pcomponent[x] != rcomponent[y]
        ]
        return reactionCenter, rreactionCenter

